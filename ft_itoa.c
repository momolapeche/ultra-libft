#include "libft.h"

char	*ft_itoa(int n)
{
	size_t	pow;
	char	*r;

	pow = n == 0 ? 1 : 0;
	while (n / ft_power(10, pow) != 0)
		pow++;
	r = ft_memalloc(n < 0 ? pow + 2 : pow + 1);
	if (n < 0)
		r[0] = '-';
	while (pow-- != 0)
	{
		r[pow + (n < 0 ? 1 : 0)] = (n < 0 ? -(n % 10) : n % 10) + '0';
		n /= 10;
	}
	return (r);
}
